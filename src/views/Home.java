    private void menCadAlteracaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menCadAlteracaoActionPerformed
        try {
            BuscaDesenvolvedores();
            BuscaClientes();
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        String[] arrayDesenvolvedores = Desenvolvedores.toArray(new String[0]);
        String[] arrayClientes = Clientes.toArray(new String[0]);
        CadastroAlteracao cadAlteracao = new CadastroAlteracao(arrayDesenvolvedores, arrayClientes, tipoUsuario);
        cadAlteracao.setVisible(true);
    }//GEN-LAST:event_menCadAlteracaoActionPerformed

