    public void BuscaDesenvolvedores () throws SQLException {
        UsuarioDao dao = new UsuarioDao();
        Desenvolvedores = dao.buscaUsuarios("Desenvolvedor");
    }
    
    public void BuscaClientes () throws SQLException {
        ClienteDao dao = new ClienteDao();
        Clientes = dao.buscaClientes();
    }
    
